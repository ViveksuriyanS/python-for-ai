# Floats in Python
# A float is a data type that represents a decimal number.
# Floats can be used to represent real numbers and are often used in mathematical calculations.
float_val = 5.0
print(type(float_val))  # Output: <class 'float'>
print(float_val)  # Output: 5.0

# You can perform various operations with floats, such as addition, subtraction, multiplication, and division.
a = 5.0
b = 2.0
print(a + b)  # Output: 7.0
print(a - b)  # Output: 3.0
print(a * b)  # Output: 10.0
print(a / b)  # Output: 2.5
