# Integers in Python
# An integer is a whole number that can be positive, negative, or zero.
# Integers do not have a decimal point and can be of arbitrary size.
# In Python, you can use the int() function to create an integer from a string or from a float.
int_val = 5
print(type(int_val))  # Output: <class 'int'>
print(int_val)  # Output: 5

# You can perform various operations with integers, such as addition, subtraction, multiplication, and division.
a = 5
b = 2

print(a + b)  # Output: 7
print(a - b)  # Output: 3
print(a * b)  # Output: 10
print(a / b)  # Output: 2.5 (division of integers results in a float)
print(a // b)  # Output: 2 (floor division results in an integer)
print(a % b)  # Output: 1 (modulo operator gives the remainder)
print(a**b)  # Output: 25 (exponentiation operator gives a raised to the power of b)
