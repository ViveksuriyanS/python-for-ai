# In Python, there are several built-in data types that are commonly used to store and manipulate data.
# Here are some of the most common data types in Python:

int_val = 5
float_val = 5.0
str_val = "Vivek"
long_str_val = """This is a long string that can span multiple lines."""
bool_val = True
print(type(int_val))  # Output: <class 'int'>
print(type(float_val))  # Output: <class 'float'>
print(type(str_val))  # Output: <class 'str'>
print(type(long_str_val))  # Output: <class 'str'>
print(type(bool_val))  # Output: <class 'bool'>
