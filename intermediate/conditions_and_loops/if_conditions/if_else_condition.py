# if...else condition
# The if...else statement is used to test a specific condition.
# If the condition is true, the block of code inside the if statement will be executed. If the condition is false, the block of code inside the else statement will be executed.

# E.g., Check if a number is positive or negative
number = 5
if number > 0:
    print("The number is positive.")
else:
    print("The number is negative.")
