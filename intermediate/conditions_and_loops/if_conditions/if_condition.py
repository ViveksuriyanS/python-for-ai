# if condition
# The if statement is used to test a specific condition. If the condition is true, the block of code inside the if statement will be executed.
# If the condition is false, the block of code will be skipped.

# E.g., Check if a number is positive
number = 5
if number > 0:
    print("The number is positive.")

# E.g., Check if a number is negative
number = -3
if number < 0:
    print("The number is negative.")
