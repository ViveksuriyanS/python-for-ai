# Function definition in Python
# A function is a reusable block of code that performs a specific task. 
# It allows you to organize your code and make it more modular. 
# In Python, you can define a function using the `def` keyword followed by the function name and parentheses.
# Here's an example of a simple function that prints a greeting message:

def greet():
    print("Hello, World!")
    print("Welcome to Python programming.")

greet() # Output: Hello, World! Welcome to Python programming.

